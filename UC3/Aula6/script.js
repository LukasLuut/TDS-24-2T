/*
Modelo lógico

Chave Candidata:
Atributos ou grupos de atributos com potencial para se tornarem uma chave primária

Chave primária?
Chave candidata escolhida para ser a primária na relação 

Chave Estrangeira 
Coluna de uma tabela que estabelece um relacionamento com a chave primária de outra tabela 
É a partir da chave estrangeira que sabemos com qual registro em outra tabela está relacionado

Chave composta 
Chave composta tem dois ou mais atributos.
Geralmente empregada quando não é possível utilizar uma única coluna de uma tabela para identificar 
de forma exclusiva seus registros

Chave substituta/surrogada 
tem valor numérico, único, adicionado a uma relação para servir como chave primária 
não possui significado para os usuários, geralmente escondida na aplicação 



*/